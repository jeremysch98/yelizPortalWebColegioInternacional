package Model;

public class Cuenta {
    private int codcue;
    private String descue, moncue,fecvencue,estcue,dnialu; 

    public Cuenta() {
    }

    public int getCodcue() {
        return codcue;
    }

    public void setCodcue(int codcue) {
        this.codcue = codcue;
    }

    public String getDescue() {
        return descue;
    }

    public void setDescue(String descue) {
        this.descue = descue;
    }

    public String getMoncue() {
        return moncue;
    }

    public void setMoncue(String moncue) {
        this.moncue = moncue;
    }

    public String getFecvencue() {
        return fecvencue;
    }

    public void setFecvencue(String fecvencue) {
        this.fecvencue = fecvencue;
    }

    public String getEstcue() {
        return estcue;
    }

    public void setEstcue(String estcue) {
        this.estcue = estcue;
    }

    public String getDnialu() {
        return dnialu;
    }

    public void setDnialu(String dnialu) {
        this.dnialu = dnialu;
    }   
}
