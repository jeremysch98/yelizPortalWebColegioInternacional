package Model;

public class Matricula {
    private int idmat;
    private String añoe,sede,fecmat,grado,dnialu;

    public Matricula() {
    }

    public int getIdmat() {
        return idmat;
    }

    public void setIdmat(int idmat) {
        this.idmat = idmat;
    }

    public String getAñoe() {
        return añoe;
    }

    public void setAñoe(String añoe) {
        this.añoe = añoe;
    }

    public String getSede() {
        return sede;
    }

    public void setSede(String sede) {
        this.sede = sede;
    }

    public String getFecmat() {
        return fecmat;
    }

    public void setFecmat(String fecmat) {
        this.fecmat = fecmat;
    }

    public String getGrado() {
        return grado;
    }

    public void setGrado(String grado) {
        this.grado = grado;
    }

    public String getDnialu() {
        return dnialu;
    }

    public void setDnialu(String dnialu) {
        this.dnialu = dnialu;
    }
   
}
